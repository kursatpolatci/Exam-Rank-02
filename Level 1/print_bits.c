void	print_bits(unsigned char octet)
{
	int	swap;

	swap = 7;
	while (swap >= 0)
	{
		if ((octet >> swap) & 1)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		swap--; 
	}
}
